class student:
    def __init__(self, name, grade, age):
        self.name = name
        self.grade = grade
        self.age = age
saravana = student('saravana', 10, 11)
subash = student('subash', 20, 273)


print(saravana.grade)
print(subash.grade)

print(saravana.name)
print(subash.name)

print(saravana.age)
print(subash.age)

